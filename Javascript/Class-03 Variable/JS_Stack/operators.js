// Arithmetic operators
console.log("arithmetic Operators");

let a = 20
let b = 10

console.log(a + "+" + b + "=" + (a+b));    //20+10=30
console.log(a  + "-" + b + "=" + (a-b));   //20-10=10
console.log(a + "*" + b + "=" + (a*b));    //0*10=200
console.log(a + "/" + b + "=" + (a/b));    //20/10=2

